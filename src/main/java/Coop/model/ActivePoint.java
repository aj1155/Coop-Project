package Coop.model;

public class ActivePoint {
	int fileUpload;
	int issueMake;
	int etc;
	int projectId;

	public int getFileUpload() {
		return fileUpload;
	}

	public void setFileUpload(int fileUpload) {
		this.fileUpload = fileUpload;
	}

	public int getIssueMake() {
		return issueMake;
	}

	public void setIssueMake(int issueMake) {
		this.issueMake = issueMake;
	}

	public int getEtc() {
		return etc;
	}

	public void setEtc(int etc) {
		this.etc = etc;
	}

	public int getProjectId() {
		return projectId;
	}

	public void setProjectId(int projectId) {
		this.projectId = projectId;
	}
	

}
